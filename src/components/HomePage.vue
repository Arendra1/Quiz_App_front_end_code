    async getQuizzes() {

      this.success = false;

      try {
        const result = await axios.get('http://localhost:8000/api/quiz/quizzes');
        const quizzes = result.data.quizzes;

        // For Recently Added Quizzes
        quizzes.sort((a, b) => b.quizId - a.quizId);
        this.recentlyAddedQuizzes = quizzes;

        // For Trending  Quizzes
        let count = 0;
        quizzes.sort((a, b) => b.attemptedBy - a.attemptedBy);
        quizzes.forEach((quiz) => {
          count++;
          if (count < 9) {
            this.trendingQuizzes.push(quiz);
          }

        });


        // For Recommended  Quizzes


        const userInfo = await axios.get('http://localhost:8000/api/user/loggeduser');
        const user = userInfo.data.user;

        // Sorting  alphabetically
        quizzes.sort((a, b) => {
          if (a.quizName > b.quizName) {
            return 1;
          } else {
            return -1;
          }
        });

        count = 0;
        quizzes.sort((a, b) => b.attemptedBy - a.attemptedBy);
        quizzes.forEach((quiz) => {
          if (count < 6 && quiz.category == user.domain) {
            this.recommmendedQuizzes.push(quiz);
            count++;
          }

        });

      }

      catch (error) {
        console.log(error.name);
        console.log(error.message);
        return false;
      }

    },


